let divisao = primeiroNumero / segundoNumero;
let modulo = primeiroNumero % segundoNumero;

console.log(
  `A soma entre ${primeiroNumero} e ${segundoNumero} é igual ${adicao}`
);

console.log(
  `A subtração entre ${primeiroNumero} e ${segundoNumero} é igual ${subtracao}`
);

console.log(
  `A multiplicação entre ${primeiroNumero} e ${segundoNumero} é igual ${multiplicacao}`
);

console.log(
  `A divisão entre ${primeiroNumero} e ${segundoNumero} é igual ${divisao}`
);

